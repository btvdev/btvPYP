package com.btvpyp.sample.model;

import java.io.Serializable;

public class FingerPush implements Serializable {
	private static final long serialVersionUID = -5437689010063298179L;
	private String sampleId;         //样本ID
	private String name;             //指纹名字
	private String programName;      //指纹节目名
	private String type;             //指纹类型, Titles – 片头, Trailer – 片尾, Ad – 广告
	private String source;           //指纹源文件路径
	private String recognizeAgain;   //是否需要二次识别，true.需要 false.不需要
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProgramName() {
		return programName;
	}
	public void setProgramName(String programName) {
		this.programName = programName;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getSampleId() {
		return sampleId;
	}
	public void setSampleId(String sampleId) {
		this.sampleId = sampleId;
	}
	public String getRecognizeAgain() {
		return recognizeAgain;
	}
	public void setRecognizeAgain(String recognizeAgain) {
		this.recognizeAgain = recognizeAgain;
	}
}
