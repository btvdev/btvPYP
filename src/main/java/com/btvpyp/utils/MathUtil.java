package com.btvpyp.utils;
/**
 * 数值计算的工具类
 * @author gaobo 2017-06-29
 *
 */
public class MathUtil {
	/**
	 * 除法做商，然后四舍五入
	 * @param beichushu  被除数
	 * @param chushu     除数
	 * @param jingdu     精度
	 * @return
	 */
	public static String divisionRound(long beichushu, double chushu, String jingdu){
		String result = "";
		double c = beichushu / chushu;
		result = String.format(jingdu, c);
		return result;
	}
}
