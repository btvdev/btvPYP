package com.btvpyp.utils;

public class KeyGen {
	public static String getADVId(){
		String ID = "";
		int ran = (int)(899*Math.random()+100);
		ID = "ADV" + System.currentTimeMillis() + ran;
		return ID;
	}
	public static void main(String args[]){
		String c = getADVId();
		System.out.println(c);
	}
}
