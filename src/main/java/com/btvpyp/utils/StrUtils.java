package com.btvpyp.utils;
/**
 * String工具类，某些公用方法
 * @author gaobo
 * 2017-08-29
 */
public class StrUtils {
	
	public static boolean isNotEmptyStr(String objStr){
		boolean re = false;
		if(null != objStr && !"".equals(objStr)){
			re = true;
		}
		return re;
	}
	
	public static void main(String args[]){
		boolean s = isNotEmptyStr("123");
		System.out.println(s);
	}
}
